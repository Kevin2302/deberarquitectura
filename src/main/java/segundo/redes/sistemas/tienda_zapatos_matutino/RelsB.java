    public void promocionHombreNike() {
        double descuento = 0, preciounit, preciototal;
        int cantidadCalzados;
        //seleccionada la marca
        if (jMarca.getSelectedIndex() == 1) {
            if (jRHombre.isSelected() == true) {
                //seleccionada las tallas
                if (jCTalla.getSelectedIndex() >= 5) {
                    descuento = 0.05;
                } else if (jCTalla.getSelectedIndex() == 6) {
                    descuento = 0.07;
                } else if (jCTalla.getSelectedIndex() == 7) {
                    descuento = 0.09;
                } else if (jCTalla.getSelectedIndex() == 8) {
                    descuento = 0.11;
                } else if (jCTalla.getSelectedIndex() == 9) {
                    descuento = 0.13;
                }

            }
            cantidadCalzados = (int) jCantidad.getValue();
            preciounit = Double.parseDouble(jTextPrecioU.getText());
            preciototal = (cantidadCalzados * preciounit) - ((cantidadCalzados * preciounit) * descuento);
            jTextPrecioTotal.setText(String.valueOf(preciototal));

        } else {
            JOptionPane.showMessageDialog(null, "Talla incorrcta", "ERROR", JOptionPane.ERROR_MESSAGE);
            jCTalla.setSelectedIndex(0);
            jTextPrecioTotal.setText("");
        }

    }

    public void promocionMujerNike() {
        double descuento = 0, preciounit, preciototal;
        int cantidadCalzados;
        //seleccionada la marca
        if (jMarca.getSelectedIndex() == 1) {
            if (jRHombre.isSelected() == true) {
                //seleccionada las tallas
                if (jCTalla.getSelectedIndex() > 0 && jCTalla.getSelectedIndex() <= 5) {
                    descuento = 0.04;
                } else if (jCTalla.getSelectedIndex() == 2) {
                    descuento = 0.06;
                } else if (jCTalla.getSelectedIndex() == 3) {
                    descuento = 0.08;
                } else if (jCTalla.getSelectedIndex() == 4) {
                    descuento = 0.10;
                } else if (jCTalla.getSelectedIndex() == 5) {
                    descuento = 0.12;
                }
            }
            cantidadCalzados = (int) jCantidad.getValue();
            preciounit = Double.parseDouble(jTextPrecioU.getText());
            preciototal = (cantidadCalzados * preciounit) - ((cantidadCalzados * preciounit) * descuento);
            jTextPrecioTotal.setText(String.valueOf(preciototal));

        } else {
            JOptionPane.showMessageDialog(null, "Talla incorrcta", "ERROR", JOptionPane.ERROR_MESSAGE);
            jTextPrecioTotal.setText("");
        }

    }

