    private void jBGenerarCompraActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBGenerarCompraActionPerformed
        // TODO add your handling code here:
        //GENERAR COMPRA
        if (jMarca.getSelectedIndex() != 0 && (jRHombre.isSelected() != false || jRMujer.isSelected() != false) && (!jTextColor.getText().equals("")) && (int) jCantidad.getValue() != 0 && (!jTextPrecioU.getText().equals(""))) {

            if (jRHombre.isSelected()) {
                promocionHombreNike();
                if (jCTalla.getSelectedIndex() >= 5) {
                    modelo.addRow(new Object[]{
                        jMarca.getSelectedItem(),
                        "HOMBRE",
                        jCTalla.getSelectedItem(),
                        jTextColor.getText(),
                        jCantidad.getValue(),
                        jTextPrecioU.getText(),
                        jTextPrecioTotal.getText()

                    });
                }

            } else if (jRMujer.isSelected()) {
                promocionMujerNike();
                if (jCTalla.getSelectedIndex() > 0 && jCTalla.getSelectedIndex() <= 5) {
                    modelo.addRow(new Object[]{
                        jMarca.getSelectedItem(),
                        "MUJER",
                        jCTalla.getSelectedItem(),
                        jTextColor.getText(),
                        jCantidad.getValue(),
                        jTextPrecioU.getText(),
                        jTextPrecioTotal.getText()
                    });
                }

            } else {
                JOptionPane.showMessageDialog(null, "DATOS INCOMPLETOS", "ERROR", JOptionPane.WARNING_MESSAGE);
            }

        }
    }//GEN-LAST:event_jBGenerarCompraActionPerformed

